# Section 11: Operators

# ARITHMETIC OPERATORS
    #   + 	    Addition
    #   - 	    Subtraction	
    #   * 	    Multiplication
    #   / 	    Division
    #   % 	    Modulus	
    #   ** 	    Exponentiation
    #   // 	    Floor division


# LOGICAL OPERATORS
    #   and  	Returns True if both statements are true
    #   or 	    Returns True if one of the statements is true
    #   not 	Reverse the result, returns False if the result is true


# IDENTITY OPERATORS
    # is  	    Returns True if both variables are the same object
    # is not    Returns True if both variables are not the same object


# MEMBERSHIP OPERATORS
    # in  	    Returns True if a sequence with the specified value is present in the object
    # not in 	Returns True if a sequence with the specified value is not present in the object


# BITWISE OPERATORS
    # &  	AND                     Sets each bit to 1 if both bits are 1
    # | 	OR 	                    Sets each bit to 1 if one of two bits is 1
    # ^ 	XOR                     Sets each bit to 1 if only one of two bits is 1
    # ~ 	NOT                     Inverts all the bits
    # << 	Zero fill left shift 	Shift left by pushing zeros in from the right and let the leftmost bits fall off
    # >> 	Signed right shift 	    Shift right by pushing copies of the leftmost bit in from the left, deleting rightmost bits


# AUGMENTED ASSIDNMENTS
    #   = 	    Assignment	
    #   += 	    Addition then assignment	
    #   -= 	    Subtraction then assignment
    #   *= 	    Multiplication then assignment	
    #   /= 	    Division then assignment	
    #   %= 	    Modulus then assignment
    #   //= 	Floor division then assignment
    #   **= 	Exponentiation then assignment
    #   &= 	    AND then assignment
    #   |= 	    OR then assignment
    #   ^= 	    XOR then assignment
    #   >>= 	Zero fill left shift then assignment
    #   <<=     Signed right shift then assignment


# ORDER OF OPERATIONS
    #   () 	                                            Parentheses 	
    #   ** 	                                            Exponentiation 	
    #   +x  -x  ~x 	                                    Unary plus, unary minus, and bitwise NOT 	
    #   *  /  //  % 	                                Multiplication, division, floor division, and modulus 	
    #   +  - 	                                        Addition and subtraction 	
    #   <<  >> 	                                        Bitwise left and right shifts 	
    #   & 	                                            Bitwise AND 	
    #   ^ 	                                            Bitwise XOR 	
    #   | 	                                            Bitwise OR 	
    #   ==  !=  >  >=  <  <=  is  is not  in  not in  	Comparisons, identity, and membership operators 	
    #   not 	                                        Logical NOT 	
    #   and 	                                        AND 	
    #   or 	                                            OR
