# Section 3: Linked List Types

# There are three main "types" of linked lists:
    # Singly linked lists:      each node has a reference to the next node
    # Doubly linked lists:      each node has a reference to the next and previous node
    # Circular linked lists:    each node has a reference to the next node, and the last node has a reference to the first node
