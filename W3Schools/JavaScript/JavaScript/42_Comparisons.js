// Logical Operators:
// AND  &&
// OR   ||
// NOT  !

// Comparison Operators:
// ==   equal
// ===  strictly equal
// !=   not equal
// !==  strictly not equal
// >    greater than
// <    less than
// >=   greater than or equal to
// <=   less than or equal to

// Ternary Operator:
// (condition) ? value1 : value2    (returns value1 if true, value2 if false)

// Nullish Coalescing Operator:
// (condition) ?? value1 : value2   (returns value1 if not null or undefined, value2 if null or undefined)

// Optional Chaining Operator:
// (condition)?.property            (returns property if condition is not null or undefined)
