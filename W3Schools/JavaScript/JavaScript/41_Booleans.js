// We can evaluate expressions to determine if they are true or false:
Boolean(10 > 9)
(10 > 9)
10 > 9

// Everything with a value is true:
Boolean(100)
Boolean(3.14)
Boolean(-15)
Boolean("Hello")
Boolean("false")
Boolean(7 + 1 + 3.14)

// Everything without a value is false:
Boolean(0)
Boolean(-0)
Boolean("")
Boolean(null)

