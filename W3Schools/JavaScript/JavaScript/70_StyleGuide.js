// VARIABLES
// Variable names should be descriptive and not too long.
// Use camelCase for variable names.
// Use UPPERCASE for constant values.
// Use a space before and after operators.
// Indentation should be 2 spaces.

// STATEMENTS
// Always end a simple statement with a semicolon.
// Put the opening bracket at the end of the first line.
// Use one space before the opening bracket.
// Use one space before the closing bracket.
// Do not end a complex statement with a semicolon.

// OBJECTS
// Use object literal notation {} for object creation.


