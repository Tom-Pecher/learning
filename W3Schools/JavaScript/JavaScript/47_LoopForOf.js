// Section 47: For Loop Of


// We can loop across all of the elements in a collection:
const cars = ["BMW", "Volvo", "Mini"];

let text1 = "";
for (let x of cars) {
  text1 += x;
}

// This can be done for strings too:
let language = "JavaScript";

let text2 = "";
for (let x of language) {
text2 += x;
}