// Section 24: String Templates

// String templates use backticks instead of quotes:
let text1 = `He's often called "Johnny"`;

// These allow multiline strings and the use of quotes within strings:
let text2 =
`The quick
brown fox
jumps over
the lazy dog`;

// We can also substitute variables into string templates:
let firstName = "John";
let lastName = "Doe";
let text3 = `Welcome ${firstName}, ${lastName}!`;