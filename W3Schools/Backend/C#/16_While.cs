// A while loop excutes a block of code as long as its condition is met:
int i = 0;
while (i < 5) 
{
  Console.WriteLine(i);
  i++;
}

// A do-while loop works the same, except it executes the code first before checking the condition:
int j = 0;
do 
{
  Console.WriteLine(j);
  j++;
}
while (j < 5);
