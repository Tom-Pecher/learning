// Section 18: Arrays


public class Main {
  public static void main(String[] args) {

  // Java arrays are mutable, fixed-size data structures that hold elements of the same type:
  String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};

  // We can access and modify elements in an array using an index:
  cars[0] = "Opel";

  // We can also access the length of an array:
  System.out.println(cars.length);
  
  }
}
