// Section 15: While Loop


public class Main {
  public static void main(String[] args) {

    int i = 0;

    // We can use a while loop to execute code until a condition is met:
    while (i < 5) {
      System.out.println(i);
      i++;
    }

    int j = 0;

    // The do-while loop first runs the code before chekcing the condition:
    do {
      System.out.println(j);
      j++;
    }
    while (j < 5);

  }
}
