// In Go, every program is part of a package: 
package main

// Import the "fmt" package, which contains functions for formatting text:
import ("fmt")

// The main function is the entry point of a Go program:
func main() {
	// Print "Hello World!" to the console using the Println function from the "fmt" package:
	fmt.Println("Hello World!")
}

// NOTE: Go uses similar syntax to C++ with {} and ;, however Go does not require ; at the end of each statement.
