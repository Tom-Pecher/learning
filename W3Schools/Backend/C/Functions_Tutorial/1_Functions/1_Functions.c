#include <stdio.h>

// Functions are pieces of code that can be reused (as in most other languages).
void myFunction() { // When defining our function, we must specify a return type and a name.
  printf("I just got executed!");
}

int main() {
  myFunction(); // Call the function
  return 0;
}
