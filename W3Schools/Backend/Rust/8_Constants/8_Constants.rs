fn main() {
    // Constants are like variables except that once they are defined, they cannot be changed or redefined.
    // They must be assigned a value and type when they are declared:
    const _BIRTHYEAR: i32 = 1980;
    const _MINUTES_PER_HOUR: i32 = 60;
}
