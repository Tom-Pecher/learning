// Section 7: Data Types

fn main() {
    // In Rust, the variable type can be infered from the value assigned to it:
    let _x = 5; // x is of type i32

    // However, it is better to define the variable type explicitly:
    let _my_num: i32 = 5;          // integer
    let _my_double: f64 = 5.99;    // float
    let _my_letter: char = 'D';    // character
    let _my_bool: bool = true;     // boolean
    let _my_text: &str = "Hello";  // string
}
