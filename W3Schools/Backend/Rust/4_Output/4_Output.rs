// Section 4: Output

fn main() {

    // Print a line of text (creates a new line):
    println!("Hello World!");

    // Print a line of text (on the same line):
    print!("Hello World! ");
    print!("I will print on the same line.");

    // We can manually create a new line using an escape character:
    println!("Hello World!\nI will print on a new line.");
}
