# Section 6: Data Types

# There are 5 basic data types in R:

# numeric
x <- 10.5
class(x)    # we can use the class() function to check the data type of a variable

# integer
x <- 1000L
class(x)

# complex
x <- 9i + 3
class(x)

# character/string
x <- "R is exciting"
class(x)

# logical/boolean
x <- TRUE
class(x)
