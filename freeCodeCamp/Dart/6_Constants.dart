// Section 6: Constants

main() {
  // Constants are variables whose values cannot be changed once they are assigned:
  const int x = 5;
  print(x);

  // Variables which are declared but not assigned are given the null value:
  int y;
  y = 10;
  print(y);
  
  // NOTE: Constants must be assigned a value when they are declared.
}
