// Section 8: Conditional Statements

main() {
  // If-Else Statement:
  var marks = 70;
  if (marks >= 90 && marks < 100) {
    print("A+ grade");
  } else if (marks >= 80 && marks < 90) {
    print("A grade");
  } else if (marks >= 70 && marks < 80) {
    print("B grade");
  } else {
    print("Invalid marks");
  }
}
